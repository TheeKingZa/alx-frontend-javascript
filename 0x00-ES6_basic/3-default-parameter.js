// Define a function called getSumOfHoods with three parameters:
// initialNumber, expansion1989, and expansion2019
export default function getSumOfHoods(initialNumber, expansion1989 = 89, expansion2019 = 19) {
  // If the parameter expansion1989 is not provided, set its value to 89
  // If the parameter expansion2019 is not provided, set its value to 19
  // Return the sum of initialNumber, expansion1989, and expansion2019
  return initialNumber + expansion1989 + expansion2019;
}
